# 依赖原FTCPPackageDesc.cpp中的TID以及包体内容的定义
# date: 2017-5-16
# powered by cgm
# 用法: 本脚本只根据FTCPPackageDesc.cpp文件,进行生成
#       生成处理函数后，可手动裁剪，以适用模块的具体需求
#       对于函数的具体定义，可以手动进一步修改    
